package primitive;

public class PrimitiveTypes {
	/*
	 * Java has 8 primitive types in four categories:
	 * 
	 *Integer Primitives Types:
	 * 	-byte (8-bit integer value) (range: -128 ~ 127)
	 * 	-short (16-bit integer value) (range: -32,786 ~ 32,767)
	 * 	-int (32-bit integer value)	(range: -2^31 ~ (2~31)-1)
	 * 	-long (64-bit integer value) (range: -2^63 ~ (2~63)-1)
	 *Floating Primitive Types:
	 * 	-float (32-bit of precision)
	 * 	-double (64-bit of precision) (same in JS)
	 *Boolean Types:
	 * 	-boolean (true or false)
	 *Character Types:
	 * 	-char (16-bit unsigned integer value represented as a character)
	 * 
	 * 		   unsigned - means no negative values
	 */
	
	public static void main(String[] args) {
		//Variable declaration requires two things!
		// data-type and variable name
		
		byte myByte;
		//assignment of value
		myByte = 2;
		
		// All together
		short myShort = 5;
	}	
}
